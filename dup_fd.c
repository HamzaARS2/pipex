int dup_fd(int fd, int fd2)
{
    if (fd == -1 || fd2 == -1)
        return (-1);
        // printf("fd: %i | fd: %i\n", fd, fd2);
    if (dup2(fd, fd2) == -1)
    {
        error_exit("wdawdad");
    }
    return (0);
}